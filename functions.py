def area(width, height):
    result = width * height
    return result
result = area(5, 6)
print(result)
result = area(5, 3)
print(result)
result = area(20, 3)
print(result)


def subtract(num1, num2):
    answer = num1 - num2
    return answer
answer = subtract(5, 2)
print(answer)
answer = subtract(20,15)
print(answer)


